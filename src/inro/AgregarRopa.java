    private void jBAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAceptarActionPerformed
        int stockMin;
        double precio;
        int cantidad;
                
        String nombre = jTFNombre.getText();
        if(nombre.length() == 0) {
            JOptionPane.showMessageDialog(this, "Favor de llenar correctamente el nombre.");
            return;
        }
        
        try {
            stockMin = Integer.parseInt(jTFStockMinimo.getText());
        } catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Solo se permiten numeros para el Stock Minimo");
            return;
        }
        
        if(stockMin <= 0) {
            JOptionPane.showMessageDialog(this, "Favor de llenar correctamente el Stock Minimo");
            return;
        }
        
        try {  
            precio = Double.parseDouble(jTFPrecio.getText());
        }catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Solo se permiten numeros para el Precio por Lavado");
            return;
        }
        
        if(precio <= 0) {
            JOptionPane.showMessageDialog(this, "Favor de llenar correctamente el Precio");
            return;
        }
        
        try {  
            cantidad = Integer.parseInt(jTCantidad.getText());
        }catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Solo se permiten numeros para la cantidad de ropa");
            return;
        }
        
        if(cantidad <= 0) {
            JOptionPane.showMessageDialog(this, "Favor de llenar correctamente la Cantidad.");
            return;
        }
                
        String descripcion = jTADescripcion.getText();
        if(descripcion.length() == 0) {
            JOptionPane.showMessageDialog(this, "Favor de llenar correctamente Descripcion de ropa");
            return;
        }
        
        ropa = new Ropa(nombre, stockMin, precio, cantidad, descripcion);
        dispose();
    }//GEN-LAST:event_jBAceptarActionPerformed

    public Ropa getRopa() {
        setVisible(true);
        return ropa;
    }
    
