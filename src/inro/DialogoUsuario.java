    private void jBAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAceptarActionPerformed
        String nombreUsuario;
        String contrasena;
        String contrasena2;
        
        nombreUsuario = jTFNombreUsuario.getText();
        contrasena = new String(jPFContrasena.getPassword());
        contrasena2 = new String(jPFContrasena2.getPassword());
        
        if(nombreUsuario.length() < 3) {
            JOptionPane.showMessageDialog(this, 
                    "El nombre de usuario debe contener al menos 3 letras",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(contrasena.length() < 5) {
            JOptionPane.showMessageDialog(this, 
                    "La contrase単a debe contener al menos 5 letras",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(!contrasena.equals(contrasena2)) {
            JOptionPane.showMessageDialog(this, "Las contrase単as no coinciden",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        usuario = new Usuario(nombreUsuario, contrasena);
        
        this.dispose();
    }//GEN-LAST:event_jBAceptarActionPerformed

    public Usuario getUsuario() {
        setVisible(true);
        return usuario;
    }
    
